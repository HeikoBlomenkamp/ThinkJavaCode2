public class MyPrintingVariables2 {

	public static void main(String[] args) {

		// Conveniently, the code for displaying a variable
		// is the same regardless of its type.

		int hour = 11;
		int minute = 59;
		System.out.print("The current time is ");
		System.out.print(hour);
		System.out.print(":");
		System.out.print(minute);
		System.out.println(".");


		System.out.println("[Chapter 2.4, Check #3.]");
	}
}
